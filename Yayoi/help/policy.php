                    <p>การเปิดเผยข้อมูลข้อมูลส่วนบุคคลของท่านมีความจำเป็นต้องเป็นไปตามข้อกำหนดของกฎหมาย กระบวนการทางกฎหมาย การดำเนินคดี และ/หรือการเรียกร้องโดยหน่วยงานของรัฐและสาธารณะ นอกจากนี้เราอาจเปิดเผยข้อมูลเกี่ยวกับท่านหากเราพิจารณาแล้วว่า เพื่อความมั่นคงของชาติ การบังคับใช้กฎหมาย หรือเรื่องอื่นๆที่มีความสำคัญต่อสาธารณะ โดยเราจะเปิดเผยเท่าที่จำเป็นและเหมาะสม</p>
                    <p>นอกจากนี้เราอาจเปิดเผยข้อมูลเกี่ยวกับท่านหากเราพิจารณาแล้วว่า การเปิดเผยข้อมูลดังกล่าวมีความจำเป็นเพื่อบังคับใช้ข้อกำหนดและเงื่อนไขในการบริการ หรือปกป้องการดำเนินงานหรือผู้ใช้ของเรา นอกจากนี้ในกรณีที่มีการปรับเปลี่ยนโครงสร้างองค์กร การควบกิจการ หรือการขายธุรกิจทั้งหมดหรือบางส่วนของเรา เราอาจโอนข้อมูลส่วนบุคคลทั้งหมดหรือบางส่วนที่เราได้รวบรวมไว้ให้แก่บุคคลภายนอกที่เกี่ยวข้อง</p>
                    <p><strong>คุกกี้และเทคโนโลยีอื่นๆ</strong></p>
                    <p>เราและผู้ขายของเราที่ให้บริการแก่เรา อาจใช้คุกกี้ เว็บบีคอน และเทคโนโลยีอื่นๆ ที่คล้ายคลึงกันในเว็บไซต์ยาโยอิ ในการรวบรวมข้อมูลเพื่อเพิ่มประสบการณ์ออนไลน์ของท่านหรือยื่นข้อเสนอของเราให้สอดคล้องกับท่าน</p>
                    <p>“คุกกี้” คือไฟล์ข้อมูลขนาดเล็กที่สร้างขึ้นจากเว็บบราวเซอร์หรืออุปกรณ์ของผู้ใช้งานอินเตอร์เน็ต และใช้เพื่อจดจำ และ/หรือรับข้อมูลเกี่ยวกับผู้ใช้งาน</p>
                    <p>“เว็บบีคอน” เป็นวัตถุหรือภาพขนาดเล็กที่ถูกติดตั้งอยู่ในหน้าเว็บ แอพพลิเคชั่น หรืออีเมล และจะใช้ในการติดตามกิจกรรม นอกจากนี้ในบางครั้งจะเรียกว่า พิกเซล และ แท็ก</p>
                    <p>เราใช้คุกกี้ เว็บบีคอน และเทคโนโลยีการติดตามอื่นๆ เพื่อรวบรวมข้อมูลตามแต่วัตถุประสงค์ที่อธิบายไว้ในนโยบายความเป็นส่วนตัวนี้ รวมถึงเราอาจนำข้อมูลที่รวบรวมไว้ด้วยเทคโนโลยีเหล่านี้กับข้อมูลที่เรารวบรวมเกี่ยวตัวท่านด้วยวิธีอื่นที่อธิบายไว้ในนโยบายความเป็นส่วนตัวนี้</p>
                    <p>เราอาจใช้เทคโนโลยีเหล่านี้เพื่อ:</p>
                    <ul>
                        <li>ระบุตัวตนหรืออุปกรณ์ของท่าน</li>
                        <li>อนุญาตให้ท่านเข้าถึงและใช้งานเว็บไซต์ยาโยอิ ด้วยการทำงานที่ถูกต้อง</li>
                        <li>ปรับปรุงผลิตภัณฑ์และบริการของเรา</li>
                        <li>ช่วยในการตรวจสอบประสิทธิภาพของเว็บไซต์ยาโยอิ (เช่น ข้อผิดพลาดเมื่อมีผู้ใช้จำนวนมาก เวลาในการโหลดหน้าเว็บ ส่วนที่นิยม อื่นๆ)</li>
                        <li>จดจำข้อมูลของท่านเพื่อความสะดวกเมื่อท่านเข้าใช้งานเว็บไซต์ยาโยอิ</li>
                        <li>ช่วยปรับปรุงให้สอดคล้องกับประสบการณ์ของท่าน</li>
                        <li>เพื่อแสดงโฆษณาตามกลุ่มเป้าหมาย และ</li>
                        <li>หรือวัตถุประสงค์อื่นๆ ที่อธิบายไว้ในนโยบายความเป็นส่วนตัวนี้ในหัวข้อ “วิธีที่เรานำข้อมูลของท่านไปใช้”</li>
                    </ul>
                    <p>ท่านสามารถใช้ตัวเลือกบนเว็บบราวเซอร์ของท่าน หากท่านไม่ต้องการรับคุกกี้หรือหากท่านต้องการตั้งค่าเว็บบราวเซอร์ให้แจ้งเตือนเมื่อท่านได้รับคุกกี้นี้ คลิกที่ “ความช่วยเหลือ” บนเว็บบราวเซอร์เพื่อเรียนรู้วิธีการตั้งค่าของคุกกี้ ถ้าท่านปิดการใช้งานคุกกี้ ท่านอาจจะไม่สามารถได้รับประโยชน์จากคุณสมบัติที่มีทั้งหมดบนเว็บไซต์</p>
                    <p><strong>นโยบายสำหรับเด็ก</strong></p>
                    <p>เว็บไซต์ยาโยอิ ได้รับการออกแบบสำหรับผู้ใช้ที่มีอายุตั้งแต่ 10 ปีขึ้นไปเท่านั้น เราไม่ได้เก็บรวบรวมข้อมูลที่สามารถระบุตัวตนได้จากเด็กอายุต่ำกว่า 10 ปี หากท่านรู้ถึงข้อมูลใดๆ ที่เราเก็บรวบรวมมาจากเด็กอายุต่ำกว่า 10 ปี กรุณาติดต่อเราโดยใช้ข้อมูลการติดต่อที่อยู่ด้านล่าง นอกจากนี้ หากเราทราบว่าเราได้เก็บรวบรวมข้อมูลจากเด็กอายุต่ำกว่า 10 ปี เราจะดำเนินการลบข้อมูลดังกล่าวโดยเร็วที่สุด</p>
                    <p><strong>เว็บไซต์ของบุคคลภายนอก</strong></p>
                    <p>เว็บไซต์ยาโยอิ อาจมีการเชื่อมโยงไปยังเว็บไซต์ของบุคคลภายนอกที่ไม่เกี่ยวข้องกับเรา หากท่านเข้าชมเว็บไซต์ที่เชื่อมโยงเหล่านี้ ท่านควรอ่านนโยบายความเป็นส่วนตัว เงื่อนไขและข้อกำหนดในการให้บริการ และนโยบายอื่นๆ ของบุคคลภายนอก เราไม่มีส่วนรับผิดชอบต่อเนื้อหา หรือนโยบายความเป็นส่วนตัว ความปลอดภัย และนโยบายต่างๆ ของบุคคลภายนอก</p>
                    <p><strong>การรักษาความปลอดภัยของข้อมูล</strong></p>
                    <p>เราใช้มาตรการรักษาความปลอดภัยที่เหมาะสมในการปกป้องข้อมูลส่วนบุคคลเพื่อรักษาข้อมูลส่วนบุคคลของท่าน ถึงแม้ว่าเราจะใช้ขั้นตอนที่สมเหตุสมผลในการปกป้องข้อมูลส่วนบุคคลของท่าน แต่โปรดทราบว่าไม่ว่าเราจะใช้ความพยายามอย่างไร ก็ไม่มีมาตรการรักษาความปลอดภัยใดที่ดีที่สุดหรือไม่มีข้อบกพร่อง และไม่มีวิธีการส่งข้อมูลทางอินเทอร์เน็ตใดที่สามารถรับประกันการแทรกซึมหรือการใช้งานผิดประเภทอื่นๆ ข้อมูลใดๆ ที่เปิดเผยทางออนไลน์มีความเสี่ยงที่จะถูกแทรกซึมและนำไปใช้ผิดวัตถุประสงค์โดยบุคคลที่ไม่ได้รับอนุญาต ดังนั้นเราไม่สามารถรับประกันความปลอดภัยได้ทั้งหมดถ้าคุณยังมีการใช้ข้อมูลส่วนบุคคล</p>
