                    <p>เราช่วยให้ท่านสามารถเก็บข้อมูลส่วนบุคคลของท่านได้ถูกต้องสมบูรณ์และเป็นข้อมูลล่าสุดได้อย่างง่ายดาย เราจะเก็บรักษาข้อมูลส่วนบุคคลของท่านไว้เป็นระยะเวลาเท่าที่จำเป็น เพื่อให้เป็นไปตามวัตถุประสงค์ที่ระบุไว้ในนโยบายความเป็นส่วนตัวนี้ เว้นแต่จะมีการขยายระยะเวลาตามที่ถูกร้องขอหรือได้รับอนุญาตตามกฎหมาย</p>
